#include "ft_ssl.h"

t_u32		md5_f(t_u32 x, t_u32 y, t_u32 z)
{
	return ((z) ^ ((x) & ((y) ^ (z))));
}

t_u32		md5_g(t_u32 x, t_u32 y, t_u32 z)
{
	return ((y) ^ ((z) & ((x) ^ (y))));
}

t_u32		md5_h(t_u32 x, t_u32 y, t_u32 z)
{
	return (((x) ^ (y)) ^ (z));
}

t_u32		md5_h2(t_u32 x, t_u32 y, t_u32 z)
{
	return ((x) ^ ((y) ^ (z)));
}

t_u32		md5_i(t_u32 x, t_u32 y, t_u32 z)
{
	return ((y) ^ ((x) | ~(z)));
}
